    public void profpic()
    {



       // final ProgressDialog loading = ProgressDialog.show(this, "Please wait...","Fetching data...",false,false);

        //Creating a json array request to get the json from our api

        StringRequest stringRequest = new StringRequest(Request.Method.POST, DATA_URL, new Response.Listener<String>() {
            @Override
            public void onResponse(String s) {

               // loading.dismiss();
                //Log.d("responce", s);

                //Displaying our grid
                try {
                    JSONObject object = new JSONObject(s);
                    array= object.getJSONArray("images");


                    Log.d("ggg",""+ array.length());

                    for(int i = 0; i<array.length(); i++) {
                        JSONObject obj = null;
                        try {
                            //getting json object from current index
                            obj = array.getJSONObject(i);

                            //getting image url and title from json object
                            url = obj.getString(TAG_IMAGE_URL);
                            //name = obj.getString(TAG_NAME);
                            Log.d("responce", url);
                            // loading album cover using Glide library
                            Picasso.with(mContext).load(url).into(prof);

                        } catch (JSONException e) {
                            e.printStackTrace();
                        }
                    }


                } catch (JSONException e) {
                    e.printStackTrace();

                }
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError volleyError) {
                Log.d("ggg", volleyError.toString());
            }
        });

        //Creating a request queue
        RequestQueue requestQueue = Volley.newRequestQueue(this);
        //Adding our request to the queue
        requestQueue.add(stringRequest);


    }
